void framebuffer_size_callback(GLFWwindow* window, int width, int height);
void mouse_callback(GLFWwindow* window, double xpos, double ypos);
void scroll_callback(GLFWwindow* window, double xoffset, double yoffset);
void processInput(GLFWwindow* window);

// settings
const unsigned int SCR_WIDTH = 1920;
const unsigned int SCR_HEIGHT = 1080;

// camera
Camera camera(glm::vec3(0.0f, 0.0f, 3.0f));
float lastX = SCR_WIDTH / 2.0f;
float lastY = SCR_HEIGHT / 2.0f;
bool firstMouse = true;

// timing
float deltaTime = 0.0f;	// time between current frame and last frame
float lastFrame = 0.0f;

// lighting
glm::vec3 lightPos(1.2f, 1.0f, 2.0f);

int main(void)
{
	GLFWwindow* window;

	/* Initialize the library */
	if (!glfwInit())
		return -1;
	/* Create a windowed mode window and its OpenGL context */
	window = glfwCreateWindow(SCR_WIDTH, SCR_HEIGHT, "Light and Material", nullptr, nullptr);
	if (!window)
	{
		glfwTerminate();
		return -1;
	}

	/* Make the window's context current */
	glfwMakeContextCurrent(window);

	/* Initialize GLEW */
    glewExperimental = GL_TRUE;
	if (glewInit() != GLEW_OK) {
		std::cerr << "GLEW init failed\n";
		return -1;
	}

    glfwSetFramebufferSizeCallback(window, framebuffer_size_callback);
    glfwSetCursorPosCallback(window, mouse_callback);
    glfwSetScrollCallback(window, scroll_callback);

    // tell GLFW to capture our mouse
    glfwSetInputMode(window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);

	glEnable(GL_DEPTH_TEST);

    VertexArray cubeVAO, lightVAO;
    VertexBuffer VBO(vertices, sizeof(vertices));

    VertexBufferLayout cubeLayout, lightLayout;
    cubeLayout.Push<float>(3); // cube position
    cubeLayout.Push<float>(3); // cube normal
    lightLayout.Push<float>(3); // light position
    lightLayout.Push<float>(3); // light normal
    cubeVAO.AddBuffer(VBO, cubeLayout);
    lightVAO.AddBuffer(VBO, cubeLayout);

	// create and compile the vertex shader and fragment shader
    std::string cubeVertexShaderSource = "resource/shaders/cube_vertex_shader.glsl";
    std::string cubeFragmentShaderSource = "resource/shaders/cube_fragment_shader.glsl";
    std::string lightVertexShaderSource = "resource/shaders/light_vertex_shader.glsl";
    std::string lightFragmentShaderSource = "resource/shaders/light_fragment_shader.glsl";

    Shader cubeShader = Shader(cubeVertexShaderSource, cubeFragmentShaderSource);
    Shader lightShader = Shader(lightVertexShaderSource, lightFragmentShaderSource);

    cubeVAO.UnBindVertexArray();
    lightVAO.UnBindVertexArray();
    VBO.UnBindVertexBuffer();

    Renderer renderer;
	/* Loop until the user closes the window */
	while (!glfwWindowShouldClose(window))
	{
        // per-frame time logic
        float currentFrame = static_cast<float>(glfwGetTime());
        deltaTime = currentFrame - lastFrame;
        lastFrame = currentFrame;

        // input
        processInput(window);

		/* Render here */
		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

        cubeShader.BindShader();

        cubeShader.SetUniform3fv("light.position", lightPos);
        cubeShader.SetUniform3fv("viewPos", camera.Position);

        // light properties
        glm::vec3 lightColor;
        lightColor.x = static_cast<float>(sin(glfwGetTime() * 2.0));
        lightColor.y = static_cast<float>(sin(glfwGetTime() * 0.7));
        lightColor.z = static_cast<float>(sin(glfwGetTime() * 1.3));
        glm::vec3 diffuseColor = lightColor * glm::vec3(0.5f); // decrease the influence
        glm::vec3 ambientColor = diffuseColor * glm::vec3(0.2f); // low influence
        cubeShader.SetUniform3fv("light.ambient", ambientColor);
        cubeShader.SetUniform3fv("light.diffuse", diffuseColor);
        cubeShader.SetUniform3f("light.specular", 1.0f, 1.0f, 1.0f);

        // material properties
        cubeShader.SetUniform3f("material.ambient", 1.0f, 0.5f, 0.31f);
        cubeShader.SetUniform3f("material.diffuse", 1.0f, 0.5f, 0.31f);
        // specular lighting doesn't have full effect on this cube's material
        cubeShader.SetUniform3f("material.specular", 0.5f, 0.5f, 0.5f); 
        cubeShader.SetUniform1f("material.shininess", 32.0f);

        // world transformation
        glm::mat4 model = glm::mat4(1.0f);
        cubeShader.SetUniformMat4fv("model", model);

        // view/projection transformations
        glm::mat4 view = camera.GetViewMatrix();
        glm::mat4 projection = glm::perspective(glm::radians(camera.Fov), (float)SCR_WIDTH / (float)SCR_HEIGHT, 0.1f, 100.0f);
        cubeShader.SetUniformMat4fv("view", view);
	    cubeShader.SetUniformMat4fv("projection", projection);

        // render the cube
        cubeVAO.BindVertexArray();
        glDrawArrays(GL_TRIANGLES, 0, 36);

        // also draw the lamp cube
        lightShader.BindShader();
        model = glm::mat4(1.0f);
        model = glm::translate(model, lightPos);
        model = glm::scale(model, glm::vec3(0.2f)); // a smaller cube
        lightShader.SetUniformMat4fv("model", model);
        lightShader.SetUniformMat4fv("view", view);
        lightShader.SetUniformMat4fv("projection", projection);

        lightVAO.BindVertexArray();
        renderer.DrawArrays(GL_TRIANGLES, 0, 36);

		/* Swap front and back buffers */
		glfwSwapBuffers(window);

		/* Poll for and process events */
		glfwPollEvents();
	}
    glfwDestroyWindow(window);

	glfwTerminate();
	return 0;
}

void processInput(GLFWwindow* window)
{
    if (glfwGetKey(window, GLFW_KEY_ESCAPE) == GLFW_PRESS)
        glfwSetWindowShouldClose(window, true);
    if (glfwGetKey(window, GLFW_KEY_W) == GLFW_PRESS)
        camera.ProcessKeyboard(FORWARD, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_S) == GLFW_PRESS)
        camera.ProcessKeyboard(BACKWARD, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_A) == GLFW_PRESS)
        camera.ProcessKeyboard(LEFT, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_D) == GLFW_PRESS)
        camera.ProcessKeyboard(RIGHT, deltaTime);
}

// glfw: whenever the window size changed (by OS or user resize) this callback function executes
void framebuffer_size_callback(GLFWwindow* window, int width, int height)
{
    // make sure the viewport matches the new window dimensions; note that width and 
    // height will be significantly larger than specified on retina displays.
    glViewport(0, 0, width, height);
}

// glfw: whenever the mouse moves, this callback is called
void mouse_callback(GLFWwindow* window, double xposIn, double yposIn)
{
    float xpos = static_cast<float>(xposIn);
    float ypos = static_cast<float>(yposIn);

    if (firstMouse)
    {
        lastX = xpos;
        lastY = ypos;
        firstMouse = false;
    }

    float xoffset = xpos - lastX;
    float yoffset = lastY - ypos; // reversed since y-coordinates go from bottom to top

    lastX = xpos;
    lastY = ypos;

    camera.ProcessMouseMovement(xoffset, yoffset);
}

// glfw: whenever the mouse scroll wheel scrolls, this callback is called
void scroll_callback(GLFWwindow* window, double xoffset, double yoffset)
{
    camera.ProcessMouseScroll(static_cast<float>(yoffset));
}